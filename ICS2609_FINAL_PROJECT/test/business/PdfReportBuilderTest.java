    // ── TC-REP-010 / FR-REP-009: not saved on server (stream-only) ────────

    @Test
    public void generateUserReport_writesOnlyToProvidedStream() throws Exception {
        // The builder must write only to the provided OutputStream, not to any file.
        // We verify this by passing a ByteArrayOutputStream and ensuring the file
        // system has no side effect — implied by the architecture, but test confirms
        // the output stream contains a complete PDF.
        ByteArrayOutputStream out = new ByteArrayOutputStream();
        builder.generateUserReport(out, Arrays.asList(makeUser("a@b.com", "Admin")),
                LOGGED_IN, HEADER, FOOTER, "Server-Side Save Test");
        assertTrue("Stream must be populated (PDF delivered to stream only)",
                out.toByteArray().length > 100);
    }

