    // ── TC-REP-002: no password in report ────────────────────────────────

    @Test
    public void generateUserReport_doesNotContainKnownPasswordHash() throws Exception {
        byte[] bytes = buildUserReport(Arrays.asList(
                makeUser("sys.admin1@school.edu", "Admin")));
        // iText encodes text in PDF streams; check raw bytes for the hash
        String raw = new String(bytes, "ISO-8859-1");
        assertFalse("PDF must not contain known password hash", raw.contains(KNOWN_HASH));
    }

    @Test
    public void generateUserReport_doesNotContainWordPassword() throws Exception {
        byte[] bytes = buildUserReport(Arrays.asList(makeUser("a@b.com", "Admin")));
        String text = extractAllText(bytes);
        assertFalse("Extracted PDF text must not contain 'password'",
                text.toLowerCase().contains("password"));
    }

    // ── TC-REP-003: only username and role in user list ───────────────────

    @Test
    public void generateUserReport_columnsAreUsernameAndRole() throws Exception {
        byte[] bytes = buildUserReport(Arrays.asList(
                makeUser("prof.turing@school.edu", "Admin")));
        String text = extractAllText(bytes);
        assertTrue("Column header 'Username / Email' must be present",
                text.contains("Username") || text.contains("Email"));
        assertTrue("Column header 'Application Role' must be present",
                text.contains("Role") || text.contains("Application"));
    }

    // ── TC-REP-004: asterisk beside logged-in admin ───────────────────────

    @Test
    public void generateUserReport_asteriskBesideLoggedInAdmin() throws Exception {
        List<User> users = new ArrayList<User>();
        users.add(makeUser(LOGGED_IN, "Admin"));
        users.add(makeUser("other.admin@school.edu", "Admin"));
        byte[] bytes = buildUserReport(users);
        String text = extractAllText(bytes);
        assertTrue("PDF must contain '*' marker for logged-in admin", text.contains("*"));
    }

    @Test
    public void generateUserReport_asteriskOnlyOnLoggedInAdmin() throws Exception {
        List<User> users = new ArrayList<User>();
        users.add(makeUser(LOGGED_IN, "Admin"));
        users.add(makeUser("other.admin@school.edu", "Admin"));
        byte[] bytes = buildUserReport(users);
        String text = extractAllText(bytes);
        // logged-in user appears with asterisk
        assertTrue("Logged-in admin must appear with asterisk",
                text.contains(LOGGED_IN + " *") || text.contains(LOGGED_IN + "*"));
        // other user must NOT have asterisk
        assertFalse("Other admin must not have asterisk",
                text.contains("other.admin@school.edu *") ||
                text.contains("other.admin@school.edu*"));
    }

    // ── TC-REP-007: landscape orientation ────────────────────────────────

    @Test
    public void generateUserReport_isLandscapeOrientation() throws Exception {
        byte[] bytes = buildUserReport(Arrays.asList(makeUser("a@b.com", "Admin")));
        PdfReader reader = new PdfReader(bytes);
        com.itextpdf.text.Rectangle pageSize = reader.getPageSizeWithRotation(1);
        reader.close();
        assertTrue("PDF page width must be greater than height (landscape)",
                pageSize.getWidth() > pageSize.getHeight());
    }

    @Test
    public void generateGradeReport_isLandscapeOrientation() throws Exception {
        List<String[]> rows = new ArrayList<String[]>();
        rows.add(new String[]{"Course", "Assignment", "100", "Graded", "90", "Nice"});
        ByteArrayOutputStream out = new ByteArrayOutputStream();
        builder.generateGradeReport(out, rows, "student01@mail.com", HEADER, FOOTER, "Test");
        PdfReader reader = new PdfReader(out.toByteArray());
        com.itextpdf.text.Rectangle pageSize = reader.getPageSizeWithRotation(1);
        reader.close();
        assertTrue("Grade report must be landscape", pageSize.getWidth() > pageSize.getHeight());
    }

