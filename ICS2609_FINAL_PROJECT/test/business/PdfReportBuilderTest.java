    // ── TC-REP-009 / FR-PDF-007/008: filename format ─────────────────────

    @Test
    public void filenameTimestamp_followsYYYYMMDDHHMMSSFormat() {
        // Verify the timestamp format string matches the SRS requirement
        String ts = new SimpleDateFormat("yyyyMMddHHmmss").format(new Date());
        assertTrue("Timestamp must be exactly 14 digits", ts.matches("\\d{14}"));
        assertTrue("Year must be 4 digits", ts.substring(0, 4).matches("\\d{4}"));
        assertTrue("Month must be 2 digits (01-12)", ts.substring(4, 6).matches("(0[1-9]|1[0-2])"));
        assertTrue("Day must be 2 digits (01-31)", ts.substring(6, 8).matches("(0[1-9]|[12]\\d|3[01])"));
    }

    @Test
    public void filenameFormat_allRecordsReport_matchesConvention() {
        String ts = new SimpleDateFormat("yyyyMMddHHmmss").format(new Date());
        String filename = "ALL_RECORDS_" + ts + ".pdf";
        assertTrue("Filename must start with report name", filename.startsWith("ALL_RECORDS_"));
        assertTrue("Filename must end with .pdf", filename.endsWith(".pdf"));
        assertTrue("Filename must contain 14-digit timestamp",
                filename.matches(".*_\\d{14}\\.pdf"));
    }

    @Test
    public void filenameFormat_gradeReport_matchesConvention() {
        String ts = new SimpleDateFormat("yyyyMMddHHmmss").format(new Date());
        String filename = "GRADE_REPORT_" + ts + ".pdf";
        assertTrue(filename.matches(".*_\\d{14}\\.pdf"));
    }

